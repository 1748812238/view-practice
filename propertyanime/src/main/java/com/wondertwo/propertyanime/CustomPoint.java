package com.wondertwo.propertyanime;

/**
 * CustomPoint自定义圆点对象类
 * Created by wondertwo on 2016/3/27.
 */
public class CustomPoint {

    private float x;
    private float y;

    public CustomPoint(float x, float y) {
        this.x = x;
        this.y = y;
    }

    public float getX() {
        return x;
    }

    public float getY() {
        return y;
    }
}
